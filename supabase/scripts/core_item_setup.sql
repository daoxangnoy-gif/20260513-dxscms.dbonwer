-- 4) สร้าง MV ใหม่ (join core_item + เพิ่ม ranking, core_item)
DROP MATERIALIZED VIEW IF EXISTS public.oos_detail_mv;
CREATE MATERIALIZED VIEW public.oos_detail_mv AS
  WITH dm AS (
    SELECT DISTINCT ON (sku_code) sku_code, vendor_code
    FROM public.data_master WHERE sku_code IS NOT NULL
    ORDER BY sku_code, updated_at DESC NULLS LAST
  ),
  vm AS (
    SELECT DISTINCT ON (vendor_code) vendor_code, vendor_name_en, trade_term, spc_name
    FROM public.vendor_master WHERE vendor_code IS NOT NULL
    ORDER BY vendor_code, updated_at DESC NULLS LAST
  ),
  ranged AS (
    SELECT rsv.sku_code, (kv.key)::text AS store_name,
      COALESCE(rsv.division,'') AS division, COALESCE(rsv.department,'') AS department,
      COALESCE(rsv.product_name_la,'') AS name_la, COALESCE(rsv.main_barcode,'') AS barcode,
      COALESCE(rsv.item_type,'') AS item_type, COALESCE(rsv.buying_status,'') AS buying,
      COALESCE(rsv.rank_sale,'') AS rank_sale
    FROM public.range_store_view rsv
    CROSS JOIN LATERAL jsonb_each(COALESCE(rsv.range_data, '{}'::jsonb)) kv
    WHERE COALESCE(kv.value->>'apply_yn','N') = 'Y'
  ),
  sa_cnt AS (SELECT sku_code, COUNT(*)::int AS cnt FROM ranged GROUP BY sku_code),
  st_store AS (
    SELECT item_id, company AS store_name, SUM(COALESCE(quantity,0)) AS qty
    FROM public.stock WHERE company IS NOT NULL AND (type_store IS NULL OR type_store <> 'DC')
    GROUP BY item_id, company
  ),
  st_dc AS (
    SELECT item_id, SUM(COALESCE(quantity,0)) AS qty
    FROM public.stock WHERE type_store = 'DC' GROUP BY item_id
  )
  SELECT
    r.division, r.department, r.store_name,
    COALESCE(stp.type_store,'') AS type_store,
    r.store_name || r.sku_code AS id_match,
    r.sku_code AS sku, r.barcode, r.name_la,
    d.vendor_code, v.spc_name,
    CASE WHEN d.vendor_code IS NULL OR d.vendor_code = '' THEN ''
         WHEN v.vendor_name_en IS NULL OR v.vendor_name_en = '' THEN d.vendor_code
         ELSE d.vendor_code || '-' || v.vendor_name_en END AS vendor,
    COALESCE(v.trade_term,'') AS teadterm,
    r.item_type, r.buying, r.rank_sale,
    COALESCE(sa.cnt,0) AS store_apply,
    COALESCE(ss.qty,0) AS stock_store, COALESCE(sd.qty,0) AS stock_dc,
    CASE WHEN COALESCE(sd.qty,0) <> 0 THEN 'DC Have stock' ELSE 'DC No Stock' END AS remark_stock,
    CASE WHEN COALESCE(ss.qty,0) =  0 THEN 'Store OOS' ELSE 'StockHaveStock' END AS remark_oos,
    ci.ranking AS ranking,
    CASE WHEN ci.id18 IS NOT NULL THEN 'Core Item' ELSE 'Normal Item' END AS core_item
  FROM ranged r
  LEFT JOIN dm d        ON d.sku_code    = r.sku_code
  LEFT JOIN vm v        ON v.vendor_code = d.vendor_code
  LEFT JOIN sa_cnt sa   ON sa.sku_code   = r.sku_code
  LEFT JOIN st_store ss ON ss.item_id    = r.sku_code AND ss.store_name = r.store_name
  LEFT JOIN st_dc sd    ON sd.item_id    = r.sku_code
  LEFT JOIN public.store_type stp ON stp.store_name = r.store_name
  LEFT JOIN public.core_item ci ON ci.id18 = r.sku_code;

CREATE UNIQUE INDEX oos_mv_uk  ON public.oos_detail_mv (store_name, sku);
CREATE INDEX oos_mv_type       ON public.oos_detail_mv (type_store);
CREATE INDEX oos_mv_store      ON public.oos_detail_mv (store_name);
CREATE INDEX oos_mv_div        ON public.oos_detail_mv (division);
CREATE INDEX oos_mv_dept       ON public.oos_detail_mv (department);
CREATE INDEX oos_mv_vendor     ON public.oos_detail_mv (vendor_code);
CREATE INDEX oos_mv_spc        ON public.oos_detail_mv (spc_name);
CREATE INDEX oos_mv_core       ON public.oos_detail_mv (core_item);
CREATE INDEX oos_mv_order      ON public.oos_detail_mv (type_store, store_name, sku);
GRANT SELECT ON public.oos_detail_mv TO anon, authenticated;

-- 5) _oos_detail: เพิ่ม output ranking, core_item (DROP ก่อน เพราะเปลี่ยน return type)
DROP FUNCTION IF EXISTS public._oos_detail(text[],text[],text[],text[],text[],text[]);
CREATE FUNCTION public._oos_detail(
  p_spc text[] DEFAULT NULL, p_vendors text[] DEFAULT NULL, p_divisions text[] DEFAULT NULL,
  p_departments text[] DEFAULT NULL, p_type_stores text[] DEFAULT NULL, p_stores text[] DEFAULT NULL
)
RETURNS TABLE (
  division text, department text, store_name text, type_store text, id_match text,
  sku text, barcode text, name_la text, vendor text, teadterm text,
  item_type text, buying text, rank_sale text, store_apply integer,
  stock_store numeric, stock_dc numeric, remark_stock text, remark_oos text,
  ranking text, core_item text
)
LANGUAGE sql STABLE SECURITY DEFINER SET search_path TO 'public' SET statement_timeout TO '60s'
AS $function$
  SELECT division, department, store_name, type_store, id_match, sku, barcode, name_la,
         vendor, teadterm, item_type, buying, rank_sale, store_apply, stock_store, stock_dc,
         remark_stock, remark_oos, ranking, core_item
  FROM public.oos_detail_mv
  WHERE (p_spc         IS NULL OR cardinality(p_spc)=0         OR spc_name    = ANY(p_spc))
    AND (p_vendors     IS NULL OR cardinality(p_vendors)=0     OR vendor_code = ANY(p_vendors))
    AND (p_divisions   IS NULL OR cardinality(p_divisions)=0   OR division    = ANY(p_divisions))
    AND (p_departments IS NULL OR cardinality(p_departments)=0 OR department  = ANY(p_departments))
    AND (p_type_stores IS NULL OR cardinality(p_type_stores)=0 OR type_store  = ANY(p_type_stores))
    AND (p_stores      IS NULL OR cardinality(p_stores)=0      OR store_name  = ANY(p_stores));
$function$;
GRANT EXECUTE ON FUNCTION public._oos_detail(text[],text[],text[],text[],text[],text[]) TO anon, authenticated;

-- 6) save_oos_snapshot: เก็บ ranking, core_item ด้วย
CREATE OR REPLACE FUNCTION public.save_oos_snapshot(
  p_week text, p_filters jsonb DEFAULT '{}'::jsonb,
  p_spc text[] DEFAULT NULL, p_vendors text[] DEFAULT NULL, p_divisions text[] DEFAULT NULL,
  p_departments text[] DEFAULT NULL, p_type_stores text[] DEFAULT NULL, p_stores text[] DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql SECURITY DEFINER SET search_path TO 'public' SET statement_timeout TO '180s'
AS $function$
DECLARE v_id uuid; v_cnt integer;
BEGIN
  DELETE FROM public.oos_snapshots WHERE week_label = p_week;
  INSERT INTO public.oos_snapshots(week_label, filters, user_id)
  VALUES (p_week, COALESCE(p_filters,'{}'::jsonb), auth.uid()) RETURNING id INTO v_id;
  INSERT INTO public.oos_snapshot_rows(
    snapshot_id, division, department, store_name, type_store, id_match,
    sku, barcode, name_la, vendor, teadterm, item_type, buying, rank_sale,
    store_apply, stock_store, stock_dc, remark_stock, remark_oos, ranking, core_item)
  SELECT v_id, division, department, store_name, type_store, id_match,
         sku, barcode, name_la, vendor, teadterm, item_type, buying, rank_sale,
         store_apply, stock_store, stock_dc, remark_stock, remark_oos, ranking, core_item
  FROM public._oos_detail(p_spc, p_vendors, p_divisions, p_departments, p_type_stores, p_stores);
  GET DIAGNOSTICS v_cnt = ROW_COUNT;
  UPDATE public.oos_snapshots SET total_rows = v_cnt WHERE id = v_id;
  RETURN jsonb_build_object('id', v_id, 'total_rows', v_cnt);
END;
$function$;
REVOKE EXECUTE ON FUNCTION public.save_oos_snapshot(text,jsonb,text[],text[],text[],text[],text[],text[]) FROM PUBLIC, anon;
GRANT  EXECUTE ON FUNCTION public.save_oos_snapshot(text,jsonb,text[],text[],text[],text[],text[],text[]) TO authenticated;

